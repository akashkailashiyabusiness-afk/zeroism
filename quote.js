        let lastIndex = -1;

        function generateQuote() {
            const quoteElement = document.getElementById("quote");
            const authorElement = document.getElementById("author");

            // Remove previous animation
            quoteElement.classList.remove("animate-in");
            authorElement.classList.remove("animate-in");

            // Trigger reflow
            void quoteElement.offsetWidth;
            void authorElement.offsetWidth;

            let randomIndex;
            do {
                randomIndex = Math.floor(Math.random() * quotes.length);
            } while (randomIndex === lastIndex && quotes.length > 1);

            lastIndex = randomIndex;
            quoteElement.innerText = `"${quotes[randomIndex].text}"`;
            authorElement.innerText = `- ${quotes[randomIndex].author}`;

            // Add animation
            quoteElement.classList.add("animate-in");
            authorElement.classList.add("animate-in");
        }

       

        // Particle animation
        function createParticles() {
            const particleContainer = document.getElementById("particles");
            const particleCount = 30;

            for (let i = 0; i < particleCount; i++) {
                const particle = document.createElement("div");
                particle.className = "particle";
                particle.style.left = `${Math.random() * 100}%`;
                particle.style.animationDuration = `${Math.random() * 10 + 5}s`;
                particle.style.animationDelay = `${Math.random() * 5}s`;
                particleContainer.appendChild(particle);
            }
        }

        // Initialize
        createParticles();
        generateQuote();
  








    
     